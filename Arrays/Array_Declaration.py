# Declaration of an Array...

# Method 1: Using square brackets []
my_array1 = [10, 20, 30] # Here array will store an integer
my_array2 = ['c', 'd', 'e'] # Here array will store character
my_array3 = [28.5, 36.5, 40.2] # Here array will store floating elements

# Method 2: Using the list() constructor
my_array4 = list([10, 20, 30])

# Method 3: Using list comprehension
my_array5 = [i for i in range(1, 8)] # Creates an array from 1 to 7

# Method 4: Creating an empty array
empty_array = []


# Method 5: Creating an array with mixed data types
my_array6 = ["Tanuja", 19, 0.2]

# Displaying the arrays
print("Array 1:",my_array1)
print("Array 2:",my_array2)
print("Array 3:",my_array3)
print("Array 4:",my_array4)
print("Array 5:",my_array5)
print("Empty Array:",empty_array)
print("Array 6:",my_array6)

